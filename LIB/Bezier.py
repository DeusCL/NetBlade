	def __init__(self,a,b,c=0):

		self.p0=a
		self.p1=b
		self.p2=c
		self.x = 0
		self.y = 0
		self.z = 0

	def SetAtU(self,u):

		a= (1-u) * (1-u)
		b= 2.0 * u * (1-u)
		c= u*u

		self.x = ( p0[0] * a ) + ( p1[0] * b ) + ( p2[0] * c )
		self.y = ( p0[1] * b ) + ( p1[1] * b ) + ( p2[1] * c )
		self.z = ( p0[2] * c ) + ( p1[2] * b ) + ( p2[2] * c )

		
